package database

type dropSeed struct {
	MobID  int
	ItemID int
	Chance float64
	MinQty int
	MaxQty int
}

func buildDrops(defs []dropSeed) []MobDrop {
	out := make([]MobDrop, 0, len(defs))
	for _, d := range defs {
		out = append(out, MobDrop{
			MobID:     uint(d.MobID),
			ItemID:    d.ItemID,
			Chance:    d.Chance,
			MinQty:    d.MinQty,
			MaxQty:    d.MaxQty,
			CreatedAt: seedNow(),
		})
	}
	return out
}

